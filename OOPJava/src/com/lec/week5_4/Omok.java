package com.lec.week5_4;

public class Omok {
	private int x;
	private int y;
	private int color;
	
	public Omok() {
		
	}
	
	public Omok(int x, int y, int color) {
		this.x = x;
		this.y = y;
		this.color = color;
	}// 값저장

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	public int getColor() {
		return color;
	}
	
}
